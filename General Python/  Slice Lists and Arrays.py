# Slice Lists and Arrays


persons = [ "John", "Marissa", "Pete", "Dayton" ]

slice = persons[0:2]
print(slice)

destination = "summer holiday at beach"
mySlice = destination[0:6]
print(mySlice)

pizzas =  ['Hawai','Pepperoni','Fromaggi','Napolitana','Diavoli']

our_slices = pizzas[2:5]
print(our_slices)

Text = 'Hello World'
slicer = Text[6:12]
print(slicer)